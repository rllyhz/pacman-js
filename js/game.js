let createNewPacman = () => {
    pacman = new Pacman(
        oneBlockSize,
        oneBlockSize,
        oneBlockSize,
        oneBlockSize,
        oneBlockSize / 5
    );
};

let gameLoop = () => {
    if (gamePaused) {
        draw();
        return;
    }

    update();
    if (!gamePaused) draw();

    if (gameFirstPlay) {
        gamePaused = true;
        gameFirstPlay = false;

        showAlert(`WELCOME to Pacman Game👋 \n------------------------- \n\nHow To Play?\n\nPress 🔼▶🔽◀ to controll the Pacman. \nPress Space button to play/pause. \n\n\nGood Luck 🫵😏`);
    }
};

let gameInterval = setInterval(gameLoop, 1000 / fps);

let restartPacmanAndGhosts = () => {
    createNewPacman();
    createGhosts();

    gamePaused = true;
};

let gameOver = async () => {
    playGameOverSound();
    showAlert("Game over! 🤪🤣🫵😏");

    latestScore = score;
    score = 0;
    currentLives = LIVES_MAX;

    // Save history
    saveLatestScore(latestScore);

    rePositionFoodLocations();
    createNewPacman();
    createGhosts();

    gamePaused = true;
};

let winGame = () => {
    gamePaused = true;

    win++;
    if (win > bestWinTimes) {
        bestWinTimes = win;
        saveWinTimes(win);
    }

    playWinSound();
    showAlert("You win! ✨🥳🎆🏆");

    rePositionFoodLocations();
    createNewPacman();
    createGhosts();
};

let onGhostCollision = () => {
    currentLives--;

    if (currentLives <= 0) {
        gameOver();
        return;
    }

    playEatGhostSound();

    restartPacmanAndGhosts();
};

let update = () => {
    pacman.moveProcess();
    pacman.eat();

    if (ateAllFoods()) {
        winGame();
        return;
    }

    updateGhosts();

    if (pacman.checkGhostCollision(ghosts)) {
        onGhostCollision();
    }
};

let drawFoods = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2) {
                createRect(
                    j * oneBlockSize + oneBlockSize / 3,
                    i * oneBlockSize + oneBlockSize / 3,
                    oneBlockSize / 3,
                    oneBlockSize / 3,
                    "#FEB897"
                );
            }
        }
    }
};

let rePositionFoodLocations = () => {
    const foodLocations = [];

    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2 || map[i][j] == 3) {
                foodLocations.push([i, j]);
            }
        }
    }

    for (let idx = 0; idx < foodLocations.length; idx++) {
        let [i, j] = foodLocations[idx];
        map[i][j] = 2;
    }
};

let ateAllFoods = () => {
    let alreadyAteAll = true;

    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2) {
                // food still exists
                alreadyAteAll = false;
            }
        }
    }

    return alreadyAteAll;
};

